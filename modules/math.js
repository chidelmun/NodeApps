exports.add = function(x,y){
	return x+y;
};


exports.subtract = function(x,y){
	return x-y;
};


exports.divide = function(x,y){
	return x/y;
};


exports.modulo = function(x,y){
	return x%y;
};

exports.max = function(x,y){
	return x > y == true;
};

exports.min = function(x,y){
	return x<y == true;
}